class Product:
    def __init__(self, product_url, store_name, product_type):
        self.name = "",
        self.price_mkd = 0,
        self.original_id = "",
        self.image_url = "",
        self.description = "",
        self.brand = "",
        self.is_available = False,
        self.availability_json = "",
        self.product_url = product_url,
        self.store_name = store_name,
        self.product_type = product_type